# 外星人颜色 if结构
alien_color = 'green'
if alien_color == 'green':
    print("Congratulations! You got 5 points!")


# 外星人颜色 if-else结构
alien_color = 'yellow'
if alien_color == 'green':
    print("Congratulations! You got 5 points!")
else:
    print("Congratulations! You got 10 points!")


# 外星人颜色 if-elif-else结构
alien_color = 'red'
if alien_color == 'green':
    print("Congratulations! You got 5 points!")
elif alien_color == 'yellow':
    print("Congratulations! You got 10 points!")
else:
    print("Congratulations! You got 15 points!")


# 人的年龄段
age = 25
if age < 2:
    print("He/She is a baby.")
elif age >= 2 and age <4:
    print("He/She is learning how to walk.")
elif age >=4 and age < 13:
    print("He/She is a child.")
elif age >= 13 and age <20:
    print("He/She is a teenager.")
elif age >= 20 and age < 65:
    print("He/She is an adult.")
elif age >= 65:
    print("He/She is an old people.")



